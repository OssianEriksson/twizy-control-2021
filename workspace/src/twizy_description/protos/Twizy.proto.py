regex = re.compile('\{\{.*?\}\}', re.S)
parsed = re.sub(regex, lambda m: str(eval(m.group()[2:][:-2].strip())), proto)

lines = parsed.split('\n')
formatted = ''
tab = 0
for line in lines:
    line = line.strip()

    if not line:
        formatted += '\n'
        continue

    if line[0] in ['}', ']']:
        tab -= 1

    formatted += '  '*tab + line + '\n'

    if line[-1] in ['{', '[']:
        tab += 1

print(formatted)
